class MedicalCenter {
  final String name;
  final String address;
  final double rating;
  final int reviews;
  final String distance;
  final String time;
  final String imageUrl;

  MedicalCenter({
    required this.name,
    required this.address,
    required this.rating,
    required this.reviews,
    required this.distance,
    required this.time,
    required this.imageUrl,
  });
}
